using System;
using System.Collections.Generic;
using System.Text;

namespace Decomposition
{
    class Point
    {
        int x;
        int y;

        public Point()
        {
            x = 0;
            y = 0;
        }

        public void setPoint(int x, int y)
        {
            this.x = x;
            this.y = y;
        }
    }
}